    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        int row = tableTacGia.getSelectedRow();
        if (row == -1){
            JOptionPane.showMessageDialog(CapNhatTacGia.this, "Vui Lòng Chọn 1 Hàng Để Xóa","Lỗi",JOptionPane.ERROR_MESSAGE);
        } else{
            int confirm = JOptionPane.showConfirmDialog(CapNhatTacGia.this, "Bạn Chắc Chắn Muốn Xóa Không!","Thông Báo",JOptionPane.YES_NO_OPTION);

            if (confirm == JOptionPane.YES_OPTION){
                int id = Integer.valueOf(String.valueOf(tableTacGia.getValueAt(row,1)));

                try {
                    tgServices.deleteTacGia(id);
                } catch (SQLException ex) {
                    Logger.getLogger(Category.class.getName()).log(Level.SEVERE, null, ex);
                }
                // lam moi
                try {
                    defaultTableModle.setRowCount(0);
                    List<TacGia> tgList = tgServices.getAllTacGia();
                    int countDelete = 1;
                    for (TacGia tg:tgList){
                        defaultTableModle.addRow(new Object[]{countDelete++,tg.getMaTG(),tg.getTenTG(), tg.getLienLac()});
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(Category.class.getName()).log(Level.SEVERE, null, ex);
                }
                // đếm số lượng
                int count11 = 0;
                count11 = tableTacGia.getRowCount();
                jTFCount.setText("");
                jTFCount.setText(Integer.toString(count11));
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

