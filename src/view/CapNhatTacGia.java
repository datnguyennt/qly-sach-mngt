    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        // random maTG
        int code = 0;
        String strCode = "";
        code = (int) Math.floor(((Math.random() * 899999) + 100000));
        strCode = String.valueOf(code);
        //
        if (jTFTenTacGia.getText().equals("") || jTFLienLac.getText().equals("")){
            JOptionPane.showMessageDialog(CapNhatTacGia.this, "Không Nhận Giá Trị Rỗng","Lỗi",JOptionPane.ERROR_MESSAGE);
        }
        else{
        int confirm = JOptionPane.showConfirmDialog(CapNhatTacGia.this, "Bạn Có Chắc Chắn Muốn Thêm Không!","Thông Báo",JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION){
                try {
                    try {
                        tg = new TacGia();
                        tgServices = new TacGiaServices();
                        tg.setMaTG(strCode);
                        tg.setTenTG(jTFTenTacGia.getText().trim());
                        tg.setLienLac(jTFLienLac.getText().trim());
                        
                        tgServices.addTacGia(tg);
                    } catch (SQLException ex) {
                        Logger.getLogger(Category.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    
                    // cập nhật lại table và đếm lại
                    jTFTenTacGia.setText("");
                    jTFLienLac.setText("");
                    
                    count = 0;
                    
                    defaultTableModle.setRowCount(0);
                    List<TacGia> tgList = tgServices.getAllTacGia();
                    for (TacGia tg:tgList){
                        defaultTableModle.addRow(new Object[]{count++,tg.getMaTG(),tg.getTenTG(), tg.getLienLac()});
                    }
                    //  đếm lại
                    jTFCount.setText("");
                    jTFCount.setText("" + tableTacGia.getRowCount());
                } catch (SQLException ex) {
                    Logger.getLogger(Category.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

