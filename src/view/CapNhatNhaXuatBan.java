    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        // random maTG
        int code = 0;
        String strCode = "";
        code = (int) Math.floor(((Math.random() * 899999) + 100000));
        strCode = String.valueOf(code);
        //
        if (jTFTenNXB.getText().equals("") || jTFDiaChi.getText().equals("") || jTFDienThoai.getText().equals("")){
            JOptionPane.showMessageDialog(CapNhatNhaXuatBan.this, "Không Nhận Giá Trị Rỗng","Lỗi",JOptionPane.ERROR_MESSAGE);
        }
        else{
        int confirm = JOptionPane.showConfirmDialog(CapNhatNhaXuatBan.this, "Bạn Có Chắc Chắn Muốn Thêm Không!","Thông Báo",JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION){
                try {
                    try {
                        nxb = new NhaXuatBan();
                        nxbServices = new NhaXuatBanServices();
                        nxb.setMaNXB(strCode);
                        nxb.setTenNXB(jTFTenNXB.getText().trim());
                        nxb.setDiaChiNXB(jTFDiaChi.getText().trim());
                        nxb.setDienThoai(jTFDienThoai.getText().trim());
                        
                        nxbServices.addNXB(nxb);
                    } catch (SQLException ex) {
                        Logger.getLogger(Category.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    
                    // cập nhật lại table và đếm lại
                    jTFTenNXB.setText("");
                    jTFDiaChi.setText("");
                    jTFDienThoai.setText("");
                    count = 1;
                    
                    defaultTableModle.setRowCount(0);
                    List<NhaXuatBan> nxbList = nxbServices.getAllNXB();
                    for (NhaXuatBan nxb:nxbList){
                        defaultTableModle.addRow(new Object[]{count++,nxb.getMaNXB(),nxb.getTenNXB(),nxb.getDiaChiNXB(),nxb.getDienThoai()});
                    }
                    //  đếm lại
                    jTFCount.setText("");
                    jTFCount.setText("" + tableNhaXuatBan.getRowCount());
                } catch (SQLException ex) {
                    Logger.getLogger(Category.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

