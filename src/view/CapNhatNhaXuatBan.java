    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        int row = tableNhaXuatBan.getSelectedRow();
        if (row == -1){
            JOptionPane.showMessageDialog(CapNhatNhaXuatBan.this, "Vui Lòng Chọn 1 Hàng Để Xóa","Lỗi",JOptionPane.ERROR_MESSAGE);
        } else{
            int confirm = JOptionPane.showConfirmDialog(CapNhatNhaXuatBan.this, "Bạn Chắc Chắn Muốn Xóa Không!","Thông Báo",JOptionPane.YES_NO_OPTION);

            if (confirm == JOptionPane.YES_OPTION){
                int id = Integer.valueOf(String.valueOf(tableNhaXuatBan.getValueAt(row,1)));

                try {
                    nxbServices.deleteNXB(id);
                } catch (SQLException ex) {
                    Logger.getLogger(Category.class.getName()).log(Level.SEVERE, null, ex);
                }
                // lam moi
                try {
                    defaultTableModle.setRowCount(0);
                    List<NhaXuatBan> nxbList = nxbServices.getAllNXB();
                    int countDelete = 1;
                    for (NhaXuatBan nxb:nxbList){
                        defaultTableModle.addRow(new Object[]{countDelete++, nxb.getMaNXB(),nxb.getTenNXB(),nxb.getDiaChiNXB(),nxb.getDienThoai()});
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(Category.class.getName()).log(Level.SEVERE, null, ex);
                }
                // đếm số lượng
                int count11 = 0;
                count11 = tableNhaXuatBan.getRowCount();
                jTFCount.setText("");
                jTFCount.setText(Integer.toString(count11));
            }
        }
    }//GEN-LAST:event_jButton5ActionPerformed

